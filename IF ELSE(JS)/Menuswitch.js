day=require("readline-sync").question("Enter your day:")
switch(day){
    case "monday":
        console.log("Kadhi-chawal")
        break
    case "tuesday":
        console.log("Rayta")
        break
    case "wednesday":
        console.log("Chole-chawal")
        break
    case "thursday":
        console.log("Pasta")
        break
    case "friday":
        console.log("Macroni")
        break
    case "saturday":
        console.log("Aalu-sabzi")
        break
    case "sunday":
        console.log("Rajmah-Chawal")
        break
}
